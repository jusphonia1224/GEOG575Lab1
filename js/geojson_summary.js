//Build an attributes array from the data
function processData(data){
    //empty array to hold attributes
    var attributes = [];

    //properties of the first feature in the dataset
    var properties = data.features[0].properties;

    //push each attribute name into attributes array
    for (var attribute in properties){
        //only take attributes with population values
        if (attribute.indexOf("Oct") > -1){
            attributes.push(attribute);
        };
    };
	
	attributes.push("Total");
	
    //check result
    //console.log(attributes);

    return attributes;
};

var attributes = processData(summaryList);

function calcPropRadius(attValue, attribute) {
    //scale factor to adjust symbol size evenly
    var scaleFactor;
	
	//scale factor different for Total or Specific Day
	if (attribute == "Total")
		scaleFactor = 1.2;
	else
		scaleFactor = 5;
	
    //area based on attribute value and scale factor
    var area = attValue * scaleFactor;
    //radius calculated based on area
    var radius = Math.sqrt(area/Math.PI);

    return radius;
};

 //function to convert markers to circle markers
function pointToLayer(feature, latlng, attributes){
    //Determine which attribute to visualize with proportional symbols
    var attribute = attributes[0];
	
	//check
    //console.log(attribute);

    //create marker options
    var options = {
        fillColor: "#fff800",
        color: "#000",
        weight: 1,
        opacity: 1,
        fillOpacity: 0.8
    };

    //For each feature, determine its value for the selected attribute
    var attValue = Number(feature.properties[attribute]);

    //Give each feature's circle marker a radius based on its attribute value
    options.radius = calcPropRadius(attValue, attribute);
	
	if (attribute == "Total"){
		if (attValue >=200){
			options.fillColor = "#ff0008";
		}
		else if (attValue >=100) {
			options.fillColor = "#ff7800";
		};
	}
	else {
		if (attValue >=50){
			options.fillColor = "#ff0008";
		}
		else if (attValue >=20) {
			options.fillColor = "#ff7800";
		};
	}

    //create circle marker layer
    var layer = L.circleMarker(latlng, options);

    //build popup content string
    var popupContent = "<p><b>From:</b> " + feature.properties.From + "</p><p><b>" + "Date:</b> " + attribute + "</p><p><b>" + "Number of cases:</b> " + feature.properties[attribute] + "</p>";

    //bind the popup to the circle marker
    layer.bindPopup(popupContent);

    //event listeners to open popup on hover
    layer.on({
        mouseover: function(){
            this.openPopup();
        },
        mouseout: function(){
            this.closePopup();
        },
		click: function (){
			$('#datapanel').html('<h4>' + feature.properties.From + '</h4><br><table><tr>');
			for (i = 0; i < attributes.length; i++) {
				$('#datapanel').append('<td>' + attributes[i] + '</td>');
			}
			$('#datapanel').append('</tr><tr>');
			for (i = 0; i < attributes.length; i++) {
				$('#datapanel').append('<td>' + feature.properties[attributes[i]] + '</td>');
			}
			$('#datapanel').append('</tr></table>');
		}
    });	
	
    //return the circle marker to the L.geoJson pointToLayer option
    return layer;
};

//Add circle markers for point features to the map
function createPropSymbols(data, map, attributes){
    //create a Leaflet GeoJSON layer and add it to the map
    L.geoJson(data, {
        pointToLayer: function(feature, latlng){
            return pointToLayer(feature, latlng, attributes);
        }
    }).addTo(map);
};

createPropSymbols(summaryList, map, attributes);

